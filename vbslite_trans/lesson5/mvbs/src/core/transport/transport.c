struct transport *transport_create(struct participant *ptcp,
				   const struct transport_info *trans_info,
				   transport_rx_cb fn)
{
	if (trans_info->ipc_j6m != NULL) {
		return transport_ipc_j6m_create(ptcp, trans_info, fn);
        } else {
		return transport_udp_create(ptcp, trans_info, fn);
        }
}

