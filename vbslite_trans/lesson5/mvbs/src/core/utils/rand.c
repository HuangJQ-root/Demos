#include <mvbs/utils/time.h>
#include <mvbs/utils/rand.h>

uint32_t mvbs_rand(void)
{
	int32_t sec		= 0;
	uint32_t nanosec	= 0;
	uint32_t rand_num	= 0;

	mvbs_get_time(&sec, &nanosec);

	rand_num = ((uint32_t)sec << 16) | nanosec;

	return rand_num;
}

